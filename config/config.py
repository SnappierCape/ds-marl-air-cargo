from pathlib import Path
import yaml

# ─────────────────────────────────────────────────────────────────────────────
# Constants and Paths
# ─────────────────────────────────────────────────────────────────────────────
BASE_DIR = Path.cwd()
CONF_DIR = BASE_DIR / "config"

# ─────────────────────────────────────────────────────────────────────────────
# Helpers
# ─────────────────────────────────────────────────────────────────────────────
def load_params(file_path: str = CONF_DIR / "params.yaml"):
    with open(file_path, "r") as file:
        params = yaml.safe_load(file)
    return params
